public class Movie {
    private String name;
    private Integer launchYear;
    private Integer oscarQuantity;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getLaunchYear() {
        return launchYear;
    }

    public void setLaunchYear(Integer launchYear) {
        this.launchYear = launchYear;
    }

    public Integer getOscarQuantity() {
        return oscarQuantity;
    }

    public void setOscarQuantity(Integer oscarQuantity) {
        this.oscarQuantity = oscarQuantity;
    }
}
